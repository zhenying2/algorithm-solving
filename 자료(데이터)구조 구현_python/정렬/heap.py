import  heapq

a=[54,88,77,6,93,17,49,10,17,77,11,31,22,44,17,20]
print('정렬전:\t',a)

heapq.heapify(a) #최소힙 만들기
print('힙:\t',a)

s=[]
while a:
    s.append(heapq.heappop(a)) #a리스트의 가장 작은 항목을 리스트 s의 맨 뒤에 추가

print('정렬후:\t',s)


